console.log("Math connected");

const mathNum = 10.5345;

console.log(Math.trunc(mathNum)); // return the given integer

console.log(Math.ceil(mathNum)); // return the immediate upper integer

console.log(Math.floor(mathNum)); // return the immediate lower integer

console.log(Math.round(mathNum)); // return the nearest integer

console.log(Math.abs(mathNum)); // return the absolute positive number

console.log(Math.pow(5, 3)); // return a to the power b

console.log(Math.max(5, 3, 456, 78)); // return largest number

console.log(Math.min(5, 3, 456, 78)); // return smallest number

console.log(Math.random()); // return random number 0-1
console.log(Math.floor(Math.random() * 6)); // return random integer 0-5
console.log(Math.floor(Math.random() * (21 - 5) + 5)); // return random integer 5-20
